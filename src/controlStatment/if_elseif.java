package controlStatment;

public class if_elseif {

	public static void main(String[] args) {
		
		int Y1 = 75 ;
		
		if(Y1>=70) {
		System.out.println("A1");

	}
		else if (Y1>=60) {
			System.out.println("A2");
		}
		
		else if (Y1>=45) {
			System.out.println("B1");
		}
		else {
			System.out.println("fail");
		}

	}
}
